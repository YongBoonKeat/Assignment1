package Sample;

public class GroceryStore {//Super class
	  String behaviour;// Variable to store behaviour
	 	
	 	public GroceryStore() {//constructor with no-argument
			this.behaviour = behaviour;
		}	
	 
		public GroceryStore(String behaviour) {//constructor with one argument
			this.behaviour = behaviour;
		}
		
		public String toString() { //overriding method
			return behaviour + " is behaviour of doing business.";
		}
}
